
package intersection;

public class Inter {
    
    int duration;
    int width;
    
    public Inter(int d, int w)
    {
        duration = d;
        width = w;
    }

    public void setDuration(int duration)
    {
        this.duration = duration;
    }

    public void setWidth(int width)
    {
        this.width = width;
    }

    public int getDuration()
    {
        return duration;
    }

    public int getWidth()
    {
        return width;
    }
}
